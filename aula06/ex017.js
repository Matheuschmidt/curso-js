var n1 = 2;
var n2 = 3;


 function soma() {
    return n1 + n2;
 }

 function subtracao(){
    return n1 - n2;
 }

 function multiplicacao(){
    return n1 * n2;
 }

 function divisao(){
    if(n2!= 0){
        return n1 / n2;
    }else{
        return "Não é possivel dividir por zero";
    }

    
 }